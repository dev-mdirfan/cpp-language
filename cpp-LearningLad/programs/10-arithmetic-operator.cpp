#include <iostream>
using namespace std;

int main()
{
    int num1 = 19, num2 = 20;
    cout<<num1<<" + "<<num2<<" = "<<num1+num2<<endl;
    cout<<num1<<" - "<<num2<<" = "<<num1-num2<<endl;
    cout<<num1<<" * "<<num2<<" = "<<num1*num2<<endl;
    cout<<num1<<" / "<<num2<<" = "<<num1/num2<<endl;
    return 0;
}